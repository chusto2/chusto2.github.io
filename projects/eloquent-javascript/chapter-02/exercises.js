function drawChessboard(x) {
var size = x;
var board = '';
for (var i = 0; i < size; i++) {
  for (var a = 0; a < size; a++) {
    if ((a + i) % 2 === 0) {
      board += ' ';
    } else {
      board += '#';
    }
  }
  board += '\n';
} 
console.log(board);
}

